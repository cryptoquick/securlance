<h1>About this site</h1>

<h2>What is Frisky Market?</h2>

<p>Frisky Market is testing two primary assumptions:</p>

<ul>
	<li>Individuals are willing to pay and get paid in cryptocurrency</li>
	<li>Artists will want to sell their art in monthly art packs, and users will purchase them to add to their collections</li>
</ul>

<p>
	Frisky Market is sort of like Patreon, with a focus on adult art, easy-to-use collections, and all paid through cryptocurrency.
</p>

<h2>Why cryptocurrency?</h2>
<p>Bitcoin and Ethereum are used in order to simplify the process of collecting payments.</p>
<p>There's no worry of chargebacks, so payment is much more straightforward.</p>
<p>Collectors will find it straightforward to purchase art packs for specific months or years.</p>

<h2>Where does my art go?</h2>

<p>Frisky Market also helps collectors keep their art in a safe place.</p>