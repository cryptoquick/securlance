<h1>About this site</h1>

<h2>What is Frisky Market?</h2>

<p>
Frisky Market is testing two primary assumptions:<br>
Individuals are willing to pay and get paid in cryptocurrency<br>
Artists will want to sell their art in monthly art packs, and users will purchase them to add to their collections
</p>

<p>
Frisky Market is sort of like a combination of Gumroad and Patreon, with a focus on adult art and all paid through cryptocurrency.
</p>

<h2>Why cryptocurrency?</h2>
<p>Bitcoin and Ethereum are used in order to simplify the process of collecting payments.</p>
<p>There's no worry of chargebacks, so pyament is much more straightforward.</p>
<p>Collectors will find it straightforward to purchase art packs for specific months or years.</p>

